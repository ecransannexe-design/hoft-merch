export function fmtMoney(n: number): string {
  const [intp, dec] = n.toFixed(2).replace('.', ',').split(',')
  const spaced = intp.replace(/\B(?=(\d{3})+(?!\d))/g, ' ')
  return `${spaced},${dec}$`
}
